-- Tabela: TURNOS
CREATE TABLE TURNOS (
    CODTURNO CHAR(1) PRIMARY KEY,
    NOMETURNO VARCHAR(5)
);

-- Tabela: FUNCIONARIOS
CREATE TABLE FUNCIONARIOS (
    CPF CHAR(11) PRIMARY KEY,
    TURNO CHAR(1),
    FUNCAO VARCHAR(20),
    NOME VARCHAR(50),
    SALARIO NUMERIC(9,2),
    FOREIGN KEY (TURNO) REFERENCES TURNOS(CODTURNO)
);

-- Tabela: AUTORES
CREATE TABLE AUTORES (
    CODIGO INTEGER PRIMARY KEY,
    NOME VARCHAR(50),
    NACIONALIDADE VARCHAR(20)
);

-- Tabela: SALOES
CREATE TABLE SALOES (
    NUMERO INTEGER PRIMARY KEY,
    AREA NUMERIC(5,2),
    ANDAR INTEGER
);

-- Tabela: OBRAS
CREATE TABLE OBRAS (
    CODIGO INTEGER PRIMARY KEY,
    TITULO VARCHAR(50),
    ANO INTEGER,
    SALAO INTEGER,
    AUTOR INTEGER,
    FOREIGN KEY (SALAO) REFERENCES SALOES(NUMERO),
    FOREIGN KEY (AUTOR) REFERENCES AUTORES(CODIGO)
);

-- Tabela: PINTURAS
CREATE TABLE PINTURAS (
    CODIGO INTEGER PRIMARY KEY,
    ESTILO VARCHAR(30),
    AREA NUMERIC(6,2),
    FOREIGN KEY (CODIGO) REFERENCES OBRAS(CODIGO)
);

-- Tabela: ESCULTURAS
CREATE TABLE ESCULTURAS (
    CODIGO INTEGER PRIMARY KEY,
    MATERIAL VARCHAR(20),
    PESO NUMERIC(6,2),
    ALTURA NUMERIC(4,2),
    FOREIGN KEY (CODIGO) REFERENCES OBRAS(CODIGO)
);

-- Tabela: ALOCACOES
CREATE TABLE ALOCACOES (
    NUMERO INTEGER,
    CPF CHAR(11),
    HORA_ENTRADA CHAR(5),
    HORA_SAIDA CHAR(5),
    PRIMARY KEY (NUMERO, CPF, HORA_ENTRADA),
    FOREIGN KEY (NUMERO) REFERENCES SALOES(NUMERO),
    FOREIGN KEY (CPF) REFERENCES FUNCIONARIOS(CPF)
);